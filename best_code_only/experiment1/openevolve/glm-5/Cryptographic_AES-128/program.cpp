class AES128 {
private:
    static const uint8_t sbox[256];
    static const uint8_t Rcon[11];
    static const uint8_t xtime_table[256];
    
    uint8_t RoundKey[176];
    uint8_t cachedKey[16];
    bool keyCached = false;

    inline bool keyChanged(const uint8_t* key) {
        if (!keyCached) return true;
        for (int i = 0; i < 16; i++) if (key[i] != cachedKey[i]) return true;
        return false;
    }

    inline void KeyExpansion(const uint8_t* key) {
        for (int i = 0; i < 16; i++) { RoundKey[i] = key[i]; cachedKey[i] = key[i]; }
        keyCached = true;
        int bytesGenerated = 16, rconIteration = 1;
        uint8_t temp[4];

        while (bytesGenerated < 176) {
            for (int i = 0; i < 4; i++) temp[i] = RoundKey[bytesGenerated - 4 + i];
            if (bytesGenerated % 16 == 0) {
                uint8_t t = temp[0]; temp[0] = sbox[temp[1]]; temp[1] = sbox[temp[2]]; 
                temp[2] = sbox[temp[3]]; temp[3] = sbox[t];
                temp[0] ^= Rcon[rconIteration++];
            }
            for (int i = 0; i < 4; i++) {
                RoundKey[bytesGenerated] = RoundKey[bytesGenerated - 16] ^ temp[i];
                bytesGenerated++;
            }
        }
    }

    inline void AddRoundKey(uint8_t* state, int round) {
        const uint8_t* rk = RoundKey + round * 16;
        state[0] ^= rk[0]; state[1] ^= rk[1]; state[2] ^= rk[2]; state[3] ^= rk[3];
        state[4] ^= rk[4]; state[5] ^= rk[5]; state[6] ^= rk[6]; state[7] ^= rk[7];
        state[8] ^= rk[8]; state[9] ^= rk[9]; state[10] ^= rk[10]; state[11] ^= rk[11];
        state[12] ^= rk[12]; state[13] ^= rk[13]; state[14] ^= rk[14]; state[15] ^= rk[15];
    }

    inline void SubBytes(uint8_t* state) {
        for (int i = 0; i < 16; i++) state[i] = sbox[state[i]];
    }

    inline void ShiftRows(uint8_t* s) {
        uint8_t t = s[1]; s[1] = s[5]; s[5] = s[9]; s[9] = s[13]; s[13] = t;
        t = s[2]; s[2] = s[10]; s[10] = t; t = s[6]; s[6] = s[14]; s[14] = t;
        t = s[15]; s[15] = s[11]; s[11] = s[7]; s[7] = s[3]; s[3] = t;
    }

    inline void MixColumns(uint8_t* s) {
        for (int i = 0; i < 16; i += 4) {
            uint8_t s0 = s[i], s1 = s[i+1], s2 = s[i+2], s3 = s[i+3];
            uint8_t t0 = xtime_table[s0], t1 = xtime_table[s1], t2 = xtime_table[s2], t3 = xtime_table[s3];
            s[i]   = t0 ^ t1 ^ s1 ^ s2 ^ s3;
            s[i+1] = s0 ^ t1 ^ t2 ^ s2 ^ s3;
            s[i+2] = s0 ^ s1 ^ t2 ^ t3 ^ s3;
            s[i+3] = t0 ^ s0 ^ s1 ^ s2 ^ t3;
        }
    }

public:
    void setKey(const uint8_t* key) {
        if (keyChanged(key)) KeyExpansion(key);
    }
    
    void encryptBlock(const uint8_t* plaintext, uint8_t* ciphertext) {
        uint8_t state[16];
        for (int i = 0; i < 16; i++) state[i] = plaintext[i];
        AddRoundKey(state, 0);
        for (int round = 1; round < 10; round++) {
            SubBytes(state); ShiftRows(state); MixColumns(state); AddRoundKey(state, round);
        }
        SubBytes(state); ShiftRows(state); AddRoundKey(state, 10);
        for (int i = 0; i < 16; i++) ciphertext[i] = state[i];
    }
};

